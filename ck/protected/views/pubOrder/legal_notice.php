<p>
Nebezpečí škody na předmětu objednávky na nás přechází až převzetím<br/>
předmětu objednávky bez vad, v případě, že předmět smlouvy budete<br/>
zasílat nebo předávat dopravci, pak okamžikem převzetí předmětu<br/>
smlouvy bez vad naší organizací od této třetí osoby v místě dodání<br/>
Záruka: 24 měsíců ode dne předání a převzetí předmětu objednávky bez<br/>
vad (vč. instalace, zaškolení, apod.). Naše organizace je oprávněna<br/>
reklamovat i vady předmětu objednávky, které jsme mohli poznat při<br/>
uzavření smlouvy či při převzetí.<br/>
</p>
<p>
Naše organizace je oprávněna u Vás vady předmětu smlouvy reklamovat i<br/>
mailem. Vy jste povinni reklamovanou vadu odstranit do 30 dnů ode<br/>
dne doručení reklamace. V případě, že budete v prodlení s odstraněním<br/>
reklamace, máme právo nechat odstranit vadu sami na Vaše náklady<br/>
(součástí těchto nákladů je i cena za toto odstranění, kterou kupující<br/>
uhradí třetí osobě<br/>
</p>
<p>
Smluvní pokuta: ve výši 0,05% z kupní ceny vč. DPH za každý den prodlení<br/>
          s dodáním předmětu objednávky</br>
          ve výši 500,- Kč za každý den prodlení s odstraněním reklamace<br/>
</p>
<p>
Přijetím této objednávky potvrzujete, že ke dni přijetí objednávky<br/>
mezi námi nebylo mezi ústně žádné utvrzení dluhu. Toto utvrzení dluhu<br/>
je možné sjednat pouze písemně, a to formou návrhu a přijetí návrhu<br/>
(vč. mailu).
</p>
<p>
Přijetím objednávky potvrzujete, že výše uvedená smluvní pokuta není<br/>
nepřiměřeně vysoká.
</p>
<p>
Vedle smluvní pokuty máme požadovat po příjemci škodu.
</p>
<p>
Vlastnické právo k předmětu smlouvy nabýváme jeho předáním v místě<br/>
plnění. Do okamžiku tohoto předání vč. převzetí nesete nebezpečí škody<br/>
na předmětu smlouvy Vy.
</p>
<p>
Přijetí této objednávky s dodatkem nebo odchylkou se vylučuje. V<br/>
případě, že ji nepřijmete písemně (i mailem) v celém rozsahu, nemá<br/>
tato objednávka povahu předsmluvní informace a nezakládá Vám jakýkoliv<br/>
nárok vůči naší organizaci.
</p>
<p>
Změny smluvního vztahu vzniklého na základě této objednávky mohou být<br/>
prováděny pouze písemnou formou (vč. mailu), jestliže tato změna<br/>
nebude provedena písemně, považuje se tato změna za neexistující.<br/>
Písemnost není dána, není-⁠li písemnost jedné strany potvrzena písemně<br/>
druhou stranou. Neplatnosti nedodržení této písemnosti se může<br/>
kterákoliv strana domáhat i poté, co bylo z tohoto smluvního vztahu<br/>
již plněno, v tomto případě v rozsahu, v jakém nebylo sjednáno<br/>
písemně, se jedná o neplatné plnění.
</p>
<p>
Veškeré úkony stran v souvislosti se smluvním vztahem vyplývajícím z<br/>
této objednávky musí být provedeny oprávněnou osobou písemně (vč.<br/>
mailu).
</p>
<p>
Při výkladu ujednání smluvního vztahu dle této objednávky se<br/>
nepřihlíží k obecným obchodním zvyklostem Vašeho oboru a k obecným<br/>
obchodním zvyklostem, pokud jste s nimi naši organizaci písemně<br/>
neseznámil nejpozději v okamžik přijetí této objednávky, nebo nám<br/>
nejsou známy z jiného důvodu.
</p>
<p>
Přijetím objednávky (písemně vč. mailu) prohlašujete, že předmět<br/>
smlouvy, který nám na základě této objednávky převádíte do našeho<br/>
vlastnictví, je ve Vašem vlastnictví a neváznou na něm žádná práva<br/>
třetích osob, a že jste oprávněni vlastnické právo k předmětu smlouvy<br/>
převést na základě řádného titulu umožňujícího vznikl našeho<br/>
vlastnického práva k předmětu smlouvy a zánik veškerých případných<br/>
práv třetích osob k předmětu smlouvy.<br/>
Dosažení shody o předmětu objednávky, ceně předmětu objednávky,<br/>
termínu plnění, záruky a dalších podmínek uvedených v této objednávce<br/>
je předpokladem pro vznik smlouvy na základě této objednávky, bez<br/>
dosažení shody o těchto ustanoveních mezi námi nevznikne smluvní<br/>
vztah.
</p>
<p>
