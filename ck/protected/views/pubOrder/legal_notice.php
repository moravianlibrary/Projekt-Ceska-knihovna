Určení množstevního, časového, územního nebo jiného rozsahu v této<br/>
objednávce je pevně určeno naší dohodou uvedenou v této objednávce a<br/>
soud není oprávněn do tohoto jakkoliv zasahovat.<br/>
Do okamžiku vzniku smluvního vztahu dle této objednávky Vám nezakládá<br/>
tato objednávka  žádný nárok na uzavření tohoto smluvního vztahu a<br/>
naší organizaci tak nevzniká z této objednávky žádná povinnost.<br/>
Okamžikem přijetí objednávky zanikají jakékoliv naše vzájemné úkony,<br/>
které se od této objednávky odlišují a které by zakládaly kterékoliv<br/>
straně nárok na náhradu škody, přijetím objednávky se tyto úkony ruší<br/>
bez nároku na náhradu škody v souvislosti s tímto zrušením bez ohledu<br/>
